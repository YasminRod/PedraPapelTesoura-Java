    public void acao(){
        int jg1 = Integer.parseInt(String.valueOf(txtJg1.getPassword()));
        int jg2 = Integer.parseInt(String.valueOf(txtJg2.getPassword()));
                        
        PedraPapelTesoura ppt = new PedraPapelTesoura();
        ppt.logicaPedraPapelTesoura(jg1, jg2);
        
        if((jg1 == 1) || (jg1 == 2) || (jg1 == 3)){
            if((jg2 == 1) || (jg2 == 2) || (jg2 == 3)){
                lblResultado.setText(ppt.getResultado());
            } else {
                lblResultado.setText(ppt.getErro());
            }
        } else {
            lblResultado.setText(ppt.getErro());
        }
        
        panResultado.setVisible(true);
    }
    
    
